        Lizozomal Hastalıklar Kongresi, 27 Nisan – 02 Mayıs 2025 tarihleri arasında
        Concorde Luxury Resort Hotel, KKTC’de düzenlenecektir.</p><br>
	<p>Ülkemizde de lizozomal hastalıklar, çocuk metabolizma uzmanlarının yanı sıra,
        birinci basamakta çalışan çocuk doktorlarının ve aile hekimlerinin günlük
        pratiklerinde önemli bir yer tutmaya devam etmektedir.</p><br>
	<p>Bu kongremizde, çeşitli kurslar, paneller, karşıt görüş oturumları, günlük
        pratiğimize yönelik olgu bazlı sunumlar ve yurt içi ve yurt dışından katılacak çok
        değerli konuşmacılar tarafından verilecek konferanslarla lizozomal hastalıkları her
        yönüyle ele almayı ve sadece çocuk metabolizma ve beslenme uzmanlarının
        değil, birinci basamakta çalışan hekimlerin bilgi dağarcığını zenginleştirmeyi
        hedefledik.</p>
