        veren firmalara, firmaların yöneticilerine ve kongremizde güler yüzleriyle,
        yorulmak bilmeden renk katan firma çalışanlarına çok teşekkür ederiz. Her zaman
        çok başarılı kongreler düzenleyen derneğimiz, bu yıl en geniş katılımlı kongreyi
        düzenlemeyi hedeflemektedir.</p><br>
